# x=10
# y=20
# w=x+y
# x=w+1.0
# y=x/2
# z=x//2
# Depois use o comando print() para cada uma das variáveis e entenda o valor de cada uma delas

x = 10
print(x) # Valor da variável x do tipo inteiro

y = 20
print(y) # Valor da variável y do tipo inteiro

w = x + y
print(w) # Resultado da soma entre as variáveis x e y do tipo inteiro

x = w + 1.0
print(x) # Resultado da soma entre a variável w e o número 1.0. Nota-se que o valor da variável x foi alterado.

y = x / 2
print(y)

z = (x // 2)
print(z)